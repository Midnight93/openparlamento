    * Cos'è openparlamento? 

E' un servizio che permette di conoscere quello che i rappresentanti discutono e decidono ogni giorno nel Parlamento e consente ai cittadini di esprimere la loro posizione con voti e commenti su ogni provvedimento.

    * A che serve? 

L'obiettivo è di rendere trasparente l'attività del Parlamento mettendo disposizione di ognuno strumenti di controllo e intervento che facilitino la rapprsentanza democratica e il confronto tra gli interessi.

