    * Da dove prendete i dati? 

I dati sono di fonte ufficiale e vengono ricavati dai siti di Camera e Senato

    * Come sono ricavate le presenze dei parlamentari? 

I dati sulle presenze si riferiscono alle votazioni elettroniche che si svolgono in Aula della Camera e del Senato dall'inizio della legislatura. Le presenze dunque non si riferiscono a tutte le possibili attività parlamentari (lavori preparatori, interventi, etc. in Aula e nelle Commissioni) ma solo al totale delle presenze nelle votazioni elettroniche in Aula.

    * Cos'è l'indice di attività di un parlamentare? 

E' un indice puramente quantitativo che prende in esame il numero e la tipologia e di atti prodotti (firmati e cofirmati) e interventi fatti dal singolo parlamentare. I dati vengono estratti giornalmente da documenti ufficiali pubblicati dai siti di Camera e Senato. Per approfondire vai nella pagina [link]

    * Chi sono i parlamentari ribelli? 

Un parlamentare è considerato ribelle quando eprime un voto diverso da quello del gruppo parlamentare a cui appartiene. Si tratta di un indicatore puramente quantitativo del grado di ribellione alla "disciplina" del gruppo.

